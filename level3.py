rows = []
for row in input_text.split('\n'):
    rows.append(row)
width = len(rows[0])
height = len(rows)
results = {}
# right, down
scenarios = [(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)]


def run(right_steps=3, down_steps=1):
    tree_hit_count = 0
    x, y = 0, 0
    while y < height:
        if rows[y][x] == '#':
            tree_hit_count += 1
        x = (x + right_steps) % width
        y += down_steps
    return tree_hit_count


results = [run(right_steps=scen[0], down_steps=scen[1]) for scen in scenarios]
# Part 1
print(results[1])

# Part 2
from functools import reduce
print(reduce(lambda x, y: x*y, results))